function addition(x, y) {
    return x + y;
}

function subtract(x, y) {
    return x - y;
}

function multiply(x, y) {
    return x * y;
}

function divide(x, y) {
    return x / y;
}

module.exports.addition = addition;
module.exports.subtract = subtract;
module.exports.divide = divide;
module.exports.multiply = multiply;